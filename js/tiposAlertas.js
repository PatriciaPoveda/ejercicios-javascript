"use strict";

//Tipos de alertas

//Alert
//despliega un cuadro de texto el cual no requiere información por parte del usuario. Una vez que la función es invocada, el mensaje de alerta aparecerá con el aviso (opcional) correspondiente. 

//window.alert("Ventana alert");

//Confirm
//es similar al  window.alert(), pero además muestra un botón de cancelación en el cuadro de texto. Los botones reflejan los valores booleanos: verdadero para OK y falso para Cancelar

//window.confirm("Ventana confirm");

//Prompt
//es generalmente utilizado para requerir información del usuario. Esta función puede tomar dos argumentos, los cuales pueden ser opcionales: el primero es el mensaje que se mostraría al usuario, y el segundo es un valor predeterminado que se mostraría en el cuadro de texto.

//window.prompt('¿Cuántos años tienes?', '21');